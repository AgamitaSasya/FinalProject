public class Tiket {
	
	String nama;
	String cbdari;
	String cbkembali;
	String tglh;
	String tglb;
	String tglt;
	String kelas;
	String harga;
	
		public Tiket(String nama, String cbdari, String cbkembali, String tglh, String tglb, String tglt, String kelas,
			String harga) {
		super();
		this.nama = nama;
		this.cbdari = cbdari;
		this.cbkembali = cbkembali;
		this.tglh = tglh;
		this.tglb = tglb;
		this.tglt = tglt;
		this.kelas = kelas;
		this.harga = harga;
	}
}
